package gettersesetters.escola;

public class Aluno {
	String nome;
	int idade;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String novoNome) {
		this.nome = novoNome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int novaIdade) {
		this.idade = novaIdade;
	}
}
